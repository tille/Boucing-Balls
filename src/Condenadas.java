
public class Condenadas {

private int x;
private int y;
private Bola bola;

    public Condenadas(int x,int y,Bola bola){
        this.x=x;
        this.y=y;
        this.bola=bola;
    }

    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }

    public Bola getBola(){
        return bola;
    }

    

}
